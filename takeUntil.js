//Returns a slice of the array with elements taken from the beginning until the callback returns true
const takeUntil = (arr, callback) => {
  const result = [];
  for (const item of arr) {
    if (callback(item)) {
      break;
    }
    result.push(item);
  }
  return result;
};
//Test function: compares two arrays
